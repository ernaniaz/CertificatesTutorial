#!/usr/bin/env bash
#=============================================================================
# Lab 05: Add custom CA
# Copy CA certificate to trust anchors
#
# Usage: ./add-custom-ca.sh
# Prerequisites: RHEL 7, 8, 9, 10, root privileges
#=============================================================================

set -e  # Exit on error
set -u  # Exit on undefined variable

#=============================================================================
# CONFIGURATION
#=============================================================================

readonly RED='\033[0;31m'
readonly GREEN='\033[0;32m'
readonly YELLOW='\033[0;33m'
readonly BLUE='\033[0;34m'
readonly CYAN='\033[0;36m'
readonly BOLD='\033[1m'
readonly DIM='\033[2m'
readonly NC='\033[0m'

#=============================================================================
# HELPER FUNCTIONS
#=============================================================================

print_header ()
{
  local text="${1}"
  local width=57
  local padding=$(( width - ${#text} ))
  local pad=""
  if [[ ${padding} -gt 0 ]]; then
    pad="$(printf '%*s' "${padding}" '')"
  fi
  echo
  echo -e "${CYAN}в”Ңв”Җ$(printf 'в”Җ%.0s' $(seq 1 ${width}))в”Җв”җ${NC}"
  echo -e "${CYAN}в”Ӯ${NC} ${BOLD}${text}${NC}${pad} ${CYAN}в”Ӯ${NC}"
  echo -e "${CYAN}в””в”Җ$(printf 'в”Җ%.0s' $(seq 1 ${width}))в”Җв”ҳ${NC}"
  echo
}

print_step ()
{
  echo
  echo -e "  ${BOLD}в–ё ${1}${NC}"
}

print_success ()
{
  echo -e "  ${GREEN}вң“${NC} ${1}"
}

print_error ()
{
  echo -e "  ${RED}вң—${NC} ${1}"
}

print_warning ()
{
  echo -e "  ${YELLOW}вҡ ${NC} ${1}"
}

print_info ()
{
  echo -e "  ${BLUE}в„№${NC} ${1}"
}

error_exit ()
{
  print_error "${1}"
  exit 1
}

trap 'error_exit "Error occurred on line ${LINENO}"' ERR

#=============================================================================
# RHEL VERSION CHECK
#=============================================================================

if ! grep -q '^ID="rhel"$' /etc/os-release 2>/dev/null; then
  error_exit "This script requires Red Hat Enterprise Linux (RHEL)."
fi
readonly RHEL_VERSION="$(grep -oP '^VERSION_ID="\K[0-9]+' /etc/os-release 2>/dev/null)"
if [[ ${RHEL_VERSION} -lt 7 || ${RHEL_VERSION} -gt 10 ]]; then
  error_exit "Unsupported RHEL version. This script requires RHEL 7, 8, 9 or 10."
fi

#=============================================================================
# MAIN
#=============================================================================

OUTPUT_DIR="output"
CA_CERT="${OUTPUT_DIR}/test-ca.crt"
TRUST_ANCHOR="/etc/pki/ca-trust/source/anchors/lab-test-ca.crt"

print_header "Lab 05: Adding Custom CA to System Trust"

# Check if running as root
if [[ ${EUID} -ne 0 ]]; then
  error_exit "Error: This script must be run as root (use sudo)"
  exit 1
fi

# Check if CA exists
if [[ ! -f "${CA_CERT}" ]]; then
  print_error "Error: CA certificate not found. Run ./create-test-ca.sh first"
  exit 1
fi

# Copy CA to trust anchors
print_info "Copying CA certificate to trust anchors..."
cp "${CA_CERT}" "${TRUST_ANCHOR}"
chmod 644 "${TRUST_ANCHOR}"

print_success "CA certificate copied to: ${TRUST_ANCHOR}"
echo
echo "Note: System trust has NOT been updated yet."
echo "Run ./update-trust.sh to update the system trust store."
