#!/usr/bin/env bash
#=============================================================================
# Lab 17: Limpieza
# Elimina archivos temporales
#
# Uso: ./cleanup.sh
# Requisitos previos: RHEL 7
#=============================================================================

set -e  # Salir en caso de error
set -u  # Salir en variable no definida

#=============================================================================
# CONFIGURACIГ“N
#=============================================================================

readonly RED='\033[0;31m'
readonly GREEN='\033[0;32m'
readonly YELLOW='\033[0;33m'
readonly BLUE='\033[0;34m'
readonly CYAN='\033[0;36m'
readonly BOLD='\033[1m'
readonly DIM='\033[2m'
readonly NC='\033[0m'

#=============================================================================
# FUNCIONES AUXILIARES
#=============================================================================

print_header ()
{
  local text="${1}"
  local width=57
  local padding=$(( width - ${#text} ))
  local pad=""
  if [[ ${padding} -gt 0 ]]; then
    pad="$(printf '%*s' "${padding}" '')"
  fi
  echo
  echo -e "${CYAN}в”Ңв”Җ$(printf 'в”Җ%.0s' $(seq 1 ${width}))в”Җв”җ${NC}"
  echo -e "${CYAN}в”Ӯ${NC} ${BOLD}${text}${NC}${pad} ${CYAN}в”Ӯ${NC}"
  echo -e "${CYAN}в””в”Җ$(printf 'в”Җ%.0s' $(seq 1 ${width}))в”Җв”ҳ${NC}"
  echo
}

print_step ()
{
  echo
  echo -e "  ${BOLD}в–ё ${1}${NC}"
}

print_success ()
{
  echo -e "  ${GREEN}вң“${NC} ${1}"
}

print_error ()
{
  echo -e "  ${RED}вң—${NC} ${1}"
}

print_warning ()
{
  echo -e "  ${YELLOW}вҡ ${NC} ${1}"
}

print_info ()
{
  echo -e "  ${BLUE}в„№${NC} ${1}"
}

error_exit ()
{
  print_error "${1}"
  exit 1
}

trap 'error_exit "Error en la lГӯnea ${LINENO}"' ERR

#=============================================================================
# VERIFICACIГ“N DE VERSIГ“N RHEL
#=============================================================================

if ! grep -q '^ID="rhel"$' /etc/os-release 2>/dev/null; then
  error_exit "Este script requiere Red Hat Enterprise Linux (RHEL)."
fi
readonly RHEL_VERSION="$(grep -oP '^VERSION_ID="\K[0-9]+' /etc/os-release 2>/dev/null)"
if [[ ${RHEL_VERSION} -ne 7 ]]; then
  error_exit "VersiГіn de RHEL no soportada. Este script requiere solo RHEL 7."
fi

#=============================================================================
# PRINCIPAL
#=============================================================================

print_header "Lab 17: Limpieza de migraciГіn"

print_warning "Esto eliminarГЎ los archivos de prueba de la migraciГіn"
print_warning "Los archivos de respaldo se conservarГЎn"
echo

read -p "ВҝContinuar? (s/N): " -n 1 -r
echo
if [[ ! ${REPLY} =~ ^[Ss]$ ]]; then
  echo "Limpieza cancelada"
  exit 0
fi

echo
print_info "Limpiando..."

# Eliminar archivos de prueba creados durante la migraciГіn
rm -f /tmp/migration-test-*

print_success "Limpieza completada"
echo
echo "Archivos de respaldo conservados en:"
if ! ls -lh /root/rhel7-cert-backup-*.tar.gz 2>/dev/null; then
  echo "  No se encontrГі ninguno"
fi
echo
echo "Para eliminar los respaldos (si ya no los necesita):"
echo "  rm /root/rhel7-cert-backup-*.tar.gz"
